require('ts-node/register');

// bootstrap runners
let boostrap = require('./utilities/bootstrap/bootstrapsLoader');

// defining the application namespace
let createNamespace = require('continuation-local-storage').createNamespace;
let namespace = createNamespace('myApp');


// Ensure we're in the project directory, so cwd-relative paths work as expected
// no matter where we actually lift from.
// > Note: This is not required in order to lift, but it is a convenient default.
process.chdir(__dirname);

// Attempt to import `sails`.
var sails;
try {
  sails = require('sails');
} catch (e) {
  console.error('To run an app using `node app.js`, you usually need to have a version of `sails` installed in the same directory as your app.');
  console.error('To do that, run `npm install sails`');
  console.error('');
  console.error('Alternatively, if you have sails installed globally (i.e. you did `npm install -g sails`), you can use `sails lift`.');
  console.error('When you run `sails lift`, your app will still use a local `./node_modules/sails` dependency if it exists,');
  console.error('but if it doesn\'t, the app will run with the global sails instead!');
  return;
}

// --â€¢
// Try to get `rc` dependency (for loading `.sailsrc` files).
var rc;
try {
  rc = require('rc');
} catch (e0) {
  console.log(e0);
  try {
    rc = require('sails/node_modules/rc');
  } catch (e1) {
    console.error('Could not find dependency: `rc`.');
    console.error('Your `.sailsrc` file(s) will be ignored.');
    console.error('To resolve this, run:');
    console.error('npm install rc --save');
    rc = function () { return {}; };
  }
}

/**
  * global error handler for all the application
  */
process.on('unhandledRejection', function (reason, p) {
  console.error(reason);
});

/**
 * starting the sails server
 */
sails.lift(rc('sails'), err => {

  /**
   * bootstrap runner
   */
  boostrap.bootstrapsLoader.loadBootsrappers().then(result => '');

});
